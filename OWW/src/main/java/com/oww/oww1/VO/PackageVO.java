package com.oww.oww1.VO;

public class PackageVO {
	int package_no, type, hall, studio, dress, makeup, discount;

	public int getPackage_no() {
		return package_no;
	}

	public void setPackage_no(int package_no) {
		this.package_no = package_no;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getHall() {
		return hall;
	}

	public void setHall(int hall) {
		this.hall = hall;
	}

	public int getStudio() {
		return studio;
	}

	public void setStudio(int studio) {
		this.studio = studio;
	}

	public int getDress() {
		return dress;
	}

	public void setDress(int dress) {
		this.dress = dress;
	}

	public int getMakeup() {
		return makeup;
	}

	public void setMakeup(int makeup) {
		this.makeup = makeup;
	}

	public int getDiscount() {
		return discount;
	}

	public void setDiscount(int discount) {
		this.discount = discount;
	}

}
